#include "minirt.h"

int	main(void)
{
	t_m4x4	m;
	t_m4x4	cofactors;

	m = (t_m4x4){
		._[0][0] = -5, ._[0][1] = 2, ._[0][2] = 6, ._[0][3] = -8,
		._[1][0] = 1, ._[1][1] = -5, ._[1][2] = 1, ._[1][3] = 8,
		._[2][0] = 7, ._[2][1] = 7, ._[2][2] = -6, ._[2][3] = -7,
		._[3][0] = 1, ._[3][1] = -3, ._[3][2] = 7, ._[3][3] = 4
	};
	for (size_t i = 0; i < 16; ++i)
		cofactors._[i / 4][i % 4] = cofactor_m4x4(&m, i / 4, i % 4);
	print_m4x4(&cofactors);
	m = inverse_m4x4(&m);
	print_m4x4(&m);
	return (0);
}
