#include "minirt.h"

int	main(void)
{
	t_m4x4	m1;
	t_m4x4	m2;

	m1 = (t_m4x4){
		._[0][0] = 1, ._[0][1] = 2, ._[0][2] = 3, ._[0][3] = 4,
		._[1][0] = 5, ._[1][1] = 6, ._[1][2] = 7, ._[1][3] = 8,
		._[2][0] = 9, ._[2][1] = 10, ._[2][2] = 11, ._[2][3] = 12,
		._[3][0] = 13, ._[3][1] = 14, ._[3][2] = 15, ._[3][3] = 16,
	};
	m2 = identity_m4x4();
	scale_m4x4_in_place(&m2, 2);
	multiply_m4x4_in_place(&m2, &m1);
	print_m4x4(&m1);
	return (0);
}
